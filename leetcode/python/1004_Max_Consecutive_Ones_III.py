class Solution:
    def longestOnes(self, nums: List[int], k: int) -> int:
        left = 0         # Левая граница "окна"
        max_len = 0      # Максимальная длина окна с допустимыми заменами
        zeros = 0        # Счётчик нулей в текущем окне

        # Двигаем правую границу окна
        for right in range(len(nums)):
            if nums[right] == 0:
                zeros += 1  # Считаем, сколько нулей в текущем окне

            # Если нулей стало больше, чем можно заменить
            while zeros > k:
                # Сдвигаем левую границу вправо
                if nums[left] == 0:
                    zeros -= 1  # Убираем ноль, который вышел из окна
                left += 1

            # После корректировки границ — обновляем максимальную длину
            max_len = max(max_len, right - left + 1)

        return max_len  # Возвращаем максимальную длину "разрешённого" окна
